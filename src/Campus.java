public class Campus {
    protected final String schoolName = "Cavite State University";
    protected final String schoolAnotherName = "DSAC Campus";

    /**
     * This function displays the school name and another name of the school
     */
    public void displayInformation() {
        System.out.println("\n" + schoolName);
        System.out.println(schoolAnotherName + "\n");
    }
}
